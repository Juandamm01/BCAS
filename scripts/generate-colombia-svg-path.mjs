import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import topology from "world-atlas/countries-110m.json" assert { type: "json" };
import { feature } from "topojson-client";

const __dirname = path.dirname(fileURLToPath(import.meta.url));

const fc = feature(topology, topology.objects.countries);
const col = fc.features.find((x) => x.properties.name === "Colombia");
if (!col) throw new Error("Colombia not found");

const ring = col.geometry.coordinates[0];
const minLng = Math.min(...ring.map((c) => c[0]));
const maxLng = Math.max(...ring.map((c) => c[0]));
const minLat = Math.min(...ring.map((c) => c[1]));
const maxLat = Math.max(...ring.map((c) => c[1]));

const vw = 1000;
/** Más alto que ancho: Colombia se ve más alargada en vertical (N-S). */
const vh = 840;
const pad = 28;
const w = vw - pad * 2;
const h = vh - pad * 2;

const project = (lng, lat) => {
  const x = pad + ((lng - minLng) / (maxLng - minLng)) * w;
  const y = pad + ((maxLat - lat) / (maxLat - minLat)) * h;
  return [x, y];
};

const maxPoints = 140;
const step = Math.max(1, Math.floor(ring.length / maxPoints));
const pts = ring.filter((_, i) => i % step === 0);

const d = `${pts
  .map((c, i) => {
    const [x, y] = project(c[0], c[1]);
    return `${i === 0 ? "M" : "L"}${x.toFixed(1)},${y.toFixed(1)}`;
  })
  .join(" ")} Z`;

const out = `// Auto-generated by scripts/generate-colombia-svg-path.mjs — no editar a mano salvo regenerar.
export const COLOMBIA_SVG_PATH = ${JSON.stringify(d)} as const;

/** Caja usada al generar COLOMBIA_SVG_PATH (proyección lineal lng/lat → viewBox). */
export const COLOMBIA_VIEW_BOUNDS = {
  minLng: ${minLng},
  maxLng: ${maxLng},
  minLat: ${minLat},
  maxLat: ${maxLat},
  viewW: ${vw},
  viewH: ${vh},
  pad: ${pad},
} as const;
`;

const target = path.join(__dirname, "..", "lib", "geo", "colombiaSvgPath.generated.ts");
fs.writeFileSync(target, out, "utf8");
console.log("Wrote", target, "d length", d.length);
